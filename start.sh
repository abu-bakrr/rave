#!/bin/bash

# Переходим в папку проекта (на случай если запуск не из нее)
cd "$(dirname "$0")"

echo "🚀 Подготовка к запуску RAVE WATCH..."

# 1. Очищаем порт 3000
echo "🧹 Очистка порта 3000..."
lsof -ti:3000 | xargs kill -9 2>/dev/null

# 2. Запускаем сервер Python в фоновом режиме
echo "📦 Запуск сервера..."
python3 app.py > server.log 2>&1 &
SERVER_PID=$!

# 3. Ждем пару секунд, чтобы сервер успел подняться
sleep 2

# 4. Проверяем, запустился ли сервер
if ps -p $SERVER_PID > /dev/null
then
   echo "✅ Сервер успешно запущен!"
else
   echo "❌ Ошибка при запуске сервера. Проверьте server.log"
   exit 1
fi

# 5. Запускаем Cloudflare Tunnel
echo "🌐 Создаю публичную ссылку..."
echo "------------------------------------------------"
echo "СКОПИРУЙТЕ ССЫЛКУ ИЗ ТЕКСТА НИЖЕ (ищите .trycloudflare.com):"
echo "------------------------------------------------"

cloudflared tunnel --url http://localhost:3000

# Если пользователь нажмет Ctrl+C, выключаем и сервер тоже
kill $SERVER_PID
